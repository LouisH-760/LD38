	public static ArrayList<Entity> LoadMap (Map map) {
		ArrayList<Entity> blocks = new ArrayList<Entity>();
		
		int[] values = map.getMap();
		
		for (int y = 0; y < map.getHeight(); y++)
			for (int x = 0; x < map.getWidth(); x++)
				if (values[x + y * map.getWidth()]  > 0)
					blocks.add(new Block(x*32, y*32).setVersion(values[x + y * map.getWidth()] - 1));
				else if (values[x + y * map.getWidth()] == -1){
					Player.player.setX(x*32);
					Player.player.setY(y*32);
				}
		
		
		return blocks;
	}
